# Escreva um expressão para ver ser uma pessoa deve ou nao pagar imposto. Pessoas com salario maior que 1200 paga imposto 

print('\n\n\t\tIMPOSTO\n\n')

salario = input('DIGITE O SEU SALARIO:')

if int(salario) > 1200: 
    print('VOSSA SENHORIA TEM QUE PAGAR IMPOSTO')
else:
    print("VOSSA SENHORIA NÃO TEM QUE PAGAR IMPOSTO")

    


